CREATE VIEW vuln_timeseries AS
SELECT
    vuln_vulnerabilities.e_id,
    vuln_vulnerabilities.v_id,
    vuln_assets.a_id,
    vuln_plugins.p_id,
    vuln_vulnerabilities.created_on,
    vuln_vulnerabilities.first_found,
    vuln_vulnerabilities.indexed,
    vuln_vulnerabilities.last_fixed,
    vuln_vulnerabilities.last_found,
    vuln_vulnerabilities.scan_started_at,
    vuln_vulnerabilities.scan_uuid,
    vuln_vulnerabilities.severity,
    vuln_vulnerabilities.severity_default_id,
    vuln_vulnerabilities.severity_id,
    vuln_vulnerabilities.severity_modification_type,
    vuln_vulnerabilities.source,
    vuln_vulnerabilities.state,
    vuln_assets.asset_agent_uuid,
    vuln_assets.asset_bios_uuid,
    vuln_assets.asset_device_type,
    vuln_assets.asset_fqdn,
    vuln_assets.asset_hostname,
    vuln_assets.asset_ipv4,
    vuln_assets.asset_ipv6,
    vuln_assets.asset_last_authenticated_results,
    vuln_assets.asset_mac_address,
    vuln_assets.asset_network_id,
    vuln_assets.asset_operating_system_0,
    vuln_assets.asset_operating_system_1,
    vuln_assets.asset_operating_system_2,
    vuln_assets.asset_operating_system_3,
    vuln_assets.asset_uuid,
    vuln_plugins.plugin_cvss3_base_score,
    vuln_plugins.plugin_cvss3_temporal_score,
    vuln_plugins.plugin_cvss_base_score,
    vuln_plugins.plugin_cvss_temporal_score,
    vuln_plugins.plugin_id,
    vuln_plugins.plugin_modification_date,
    vuln_plugins.plugin_name,
    vuln_plugins.plugin_patch_publication_date,
    vuln_plugins.plugin_publication_date,
    vuln_plugins.plugin_type,
    vuln_plugins.plugin_vpr_score,
    vuln_plugins.plugin_vpr_updated,
    vuln_plugins.plugin_vuln_publication_date
FROM ((vuln_vulnerabilities
INNER JOIN vuln_assets ON vuln_vulnerabilities.e_id = vuln_assets.e_id)
INNER JOIN vuln_plugins ON vuln_vulnerabilities.e_id = vuln_plugins.e_id);